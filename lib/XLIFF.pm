=head1 NAME

XLIFF - XML Localisation Interchange File Format

=head1 SYNOPSIS

    use XLIFF;

=head1 DESCRIPTION

XLIFF is an XML-based format created to standardize localization.
XLIFF was standardized by OASIS in 2002. Its current specification is v1.2.

